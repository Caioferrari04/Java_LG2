public class Carga {
    private Integer peso;
    private Integer numero;

    public Carga(Integer peso, Integer numero) {
        this.peso = peso;
        this.numero = numero;
    }

    public Integer getPeso() {
        return this.peso;
    }

    public void setPeso(Integer peso) {
        this.peso = peso;
    }

    public Integer getNumero() {
        return this.numero;
    }

    public void setNumero(Integer numero) {
        this.numero = numero;
    }
}
